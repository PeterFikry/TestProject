package HomeWorkJan072023;

public class numbers_11D {
    public static void main(String[] args) {

        int num = 21;
        while (num <= 50) {
            System.out.println(num);
            num += 2;
        }
        System.out.println("---------------");

        int num1 = 49;
        while (num1 >= 21) {
            System.out.println(num1);
            num1 -= 2;
        }
    }
}


